//Tipos de datos

//Tipo de dato: Number

let edad = 24; //Variable de tipo número(guarda información)
let precio = 199.99; //Variable de tipo número con decimales
console.log("Number:", edad); //Imprime la edad en la consola
console.log(precio);
console.log(typeof precio); //Muestra el tipo de dato de la variable precio

